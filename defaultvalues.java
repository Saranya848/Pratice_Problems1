package datatypes;

public class defaultvalues {
	   static boolean value1;
	   static double value2;
	   static float value3;
	   static int value4;
	   static long value5;
	   static String value6;
	public static void main(String[] args) {
	      System.out.println("boolean = " + value1);
	      System.out.println("double = " + value2);
	      System.out.println("float = " + value3);
	      System.out.println("int = " + value4);
	      System.out.println("long = " + value5);
	      System.out.println("String = " + value6);

	}

}
